/* TIPOS DE DATOS
 */

//numero - number
console.log(1000);
console.log(10.5);

//texto - string
console.log("hola mundo");
console.log("hola mundo");
console.log(`hola mundo`);

//logicos TRUE OR FALSE - boolean
console.log(true);
console.log(false);

//indefinido - undefined (se usa cuando una variable no tiene una definicion)
console.log(undefined);

//nulo - null (se usa cuando una variable no tiene ningun valor)
console.log(null);

/*
 TIPOS DE DATOS ESTRUCTURALES
*/

//objetos - object
console.log({
  nombre: "karen",
  edad: 30,
  fechaDeNacimiento: null,
});

//tipos de dato lista - array
console.log(["karen", 30, null, true, undefined]);

